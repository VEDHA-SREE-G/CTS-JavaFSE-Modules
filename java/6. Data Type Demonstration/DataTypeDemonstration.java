public class DataTypeDemonstration {
    public static void main(String args[]){
        int intVal= 58;
        float floatVal = 3.1416f;
        double doubleVal = 99.9999;
        char charVal = 'v';
        boolean boolVal = true;
        System.out.println("Data Type Demonstration");
        System.out.println("Integer Data: " + intVal);
        System.out.println("Float Data: " + floatVal);
        System.out.println("Double Data: " + doubleVal);
        System.out.println("Character Data: " + charVal);
        System.out.println("Boolean Data: " + boolVal);
    }
}
